func marshalCatalog(code string, keys []string) string {
	lang := Normalize(code)
	merged := make(map[string]string, len(translations[DefaultLanguage])+len(translations[lang]))
	if len(keys) == 0 {
		for key, value := range translations[DefaultLanguage] {
			merged[key] = value
		}
		for key, value := range translations[lang] {
			merged[key] = value
		}
	} else {
		for _, key := range keys {
			merged[key] = T(lang, key)
		}
	}
	out, err := json.Marshal(merged)
	if err != nil {
		return "{}"
	}
	return string(out)
}

func Cookie(value string, secure bool) *http.Cookie {
	return &http.Cookie{
		Name:     CookieName,
		Value:    Normalize(value),
		Path:     "/",
		MaxAge:   60 * 60 * 24 * 365,
		SameSite: http.SameSiteLaxMode,
		Secure:   secure,
		HttpOnly: false,
	}
}

