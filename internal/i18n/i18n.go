const (
	DefaultLanguage = "zh"
	CookieName      = "vfl_lang"
)

type Language struct {
	Code       string
	HTML       string
	Name       string
	ShortLabel string
}

var supported = []Language{
	{Code: "zh", HTML: "zh-CN", Name: "中文", ShortLabel: "中"},
	{Code: "en", HTML: "en", Name: "English", ShortLabel: "EN"},
}

var translations = map[string]map[string]string{
	"zh": zhCN,
	"en": enUS,
}

func SupportedLanguages() []Language {
	out := make([]Language, len(supported))
	copy(out, supported)
	return out
}

func Normalize(raw string) string {
	value := strings.ToLower(strings.TrimSpace(raw))
	value = strings.ReplaceAll(value, "_", "-")
	if value == "" {
		return DefaultLanguage
	}
	switch {
	case value == "zh", value == "zh-cn", value == "zh-hans", strings.HasPrefix(value, "zh-"):
		return "zh"
	case value == "en", value == "en-us", value == "en-gb", strings.HasPrefix(value, "en-"):
		return "en"
	default:
		return DefaultLanguage
	}
}

func FromRequest(r *http.Request) string {
	if r == nil {
		return DefaultLanguage
	}
	if query := strings.TrimSpace(r.URL.Query().Get("lang")); query != "" {
		return Normalize(query)
	}
	if cookie, err := r.Cookie(CookieName); err == nil {
		return Normalize(cookie.Value)
	}
	if header := strings.TrimSpace(r.Header.Get("Accept-Language")); header != "" {
		for _, part := range strings.Split(header, ",") {
			code := strings.TrimSpace(strings.Split(part, ";")[0])
			if code == "" {
				continue
			}
			lang := Normalize(code)
			if lang != DefaultLanguage || strings.HasPrefix(strings.ToLower(code), DefaultLanguage) {
				return lang
			}
		}
	}
	return DefaultLanguage
}

func LanguageInfo(code string) Language {
	normalized := Normalize(code)
	for _, lang := range supported {
		if lang.Code == normalized {
			return lang
		}
	}
	return supported[0]
}

func T(code, key string) string {
	lang := Normalize(code)
	if dict, ok := translations[lang]; ok {
		if value, ok := dict[key]; ok {
			return value
		}
	}
	if dict, ok := translations[DefaultLanguage]; ok {
		if value, ok := dict[key]; ok {
			return value
		}
	}
	return key
}

